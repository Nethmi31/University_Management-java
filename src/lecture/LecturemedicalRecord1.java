    public void Load(){
       lecMedicalDAOimp sImp = new lecMedicalDAOimp();
        List<lecMedical> list=sImp.List();
        DefaultTableModel df=(DefaultTableModel)medical.getModel();
        df.setRowCount(0);
        for(lecMedical Nt: list){
            String NID=Nt.getStu_id();
            String NsubId=Nt.getSub_id();
            String Nday=Nt.getDay();
            String Ntype=Nt.getType();
            Integer Nhour=Nt.getHours();
            
           // String NDate=Nt.getNoticeNdate();
            //String NTitle=Nt.getNoticeNtitle();
            df.addRow(new Object[]{NID,NsubId, Nday,Ntype,Nhour});

        }
}
