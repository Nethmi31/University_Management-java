    public void Load(){
       lecCourseDAOimp sImp = new lecCourseDAOimp();
        List<lecCourse> list=sImp.List();
        DefaultTableModel df=(DefaultTableModel)course.getModel();
        df.setRowCount(0);
        for(lecCourse Nt: list){
            String depid=Nt.getDep_id();
            String subId=Nt.getSub_id();
            String subcoid=Nt.getCo_id();
            String subname=Nt.getSub_name();
            String lec_id=Nt.getLec_id();
            
           // String NDate=Nt.getNoticeNdate();
            //String NTitle=Nt.getNoticeNtitle();
            df.addRow(new Object[]{depid,subId,subcoid,subname,lec_id});

        }
}

