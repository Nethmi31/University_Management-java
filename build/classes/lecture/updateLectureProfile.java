       Lecturer l1=new Lecturer();
       String lec_id=lid.getText();
       String depid=did.getText();
       String firstname=fname.getText();
       String Lastname=lname.getText();
       String DOB=dob.getText();
       String ADDRESS=address.getText();
       String Gender=gender.getText();
       Integer Mobile=Integer.parseInt(mobile.getText());
       String Email=email.getText();
       String Position=position.getText();
       
       l1.setLec_id(lec_id);
       l1.setLec_dept_id(depid);
       l1.setLec_fname(firstname);
       l1.setLec_lname(Lastname);
       l1.setLec_dob(DOB);
       l1.setLec_address(ADDRESS);
       l1.setGender(Gender);
       l1.setLec_p_no(Mobile);
       l1.setLec_email(Email);
       l1.setLec_position(Position);
      
       
       LecturerDAOimp dao=new LecturerDAOimp();
       dao.update(l1);
       Load();
       lid.setText("");
       did.setText("");
       fname.setText("");
       lname.setText("");
       dob.setText("");
       address.setText("");
       gender.setText("");
       mobile.setText("");
       email.setText("");
       position.setText("");
       lid.requestFocus();
       
       
       
       
       
       
