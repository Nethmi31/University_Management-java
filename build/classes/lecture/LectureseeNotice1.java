public void Load(){
       lecNoticeDAOimp sImp = new lecNoticeDAOimp();
        List<lecNotice> list=sImp.List();
        DefaultTableModel df=(DefaultTableModel)notice.getModel();
        df.setRowCount(0);
        for(lecNotice Nt: list){
            String NID=Nt.getNotice_id();
            String NDepId=Nt.getNotice_date();
            String Nfname=Nt.getNotice_title();
            String Nlname=Nt.getNotice_content();
            
           // String NDate=Nt.getNoticeNdate();
            //String NTitle=Nt.getNoticeNtitle();
            df.addRow(new Object[]{NID,NDepId, Nfname,Nlname});

        }
}
    
    
